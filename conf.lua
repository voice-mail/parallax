function love.conf(t)
    t.window.title = "Parallax Image Viewer"
    t.window.width = 640
    t.window.height = 480
    t.window.resizable = false
    t.window.vsync = 1
end








settings = {
    -------------------------- EDITABLE SETTINGS --------------------------
    scrollSpeed = 0.5,          -- Number of pixels per frame the background scrolls
    speedMultiplier = 2,        -- Multiplier for speed of parallax effect
}









